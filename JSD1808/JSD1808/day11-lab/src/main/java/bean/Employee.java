package bean;

public class Employee {
	private String ename;
	private int salary;
	private int age;
	
	public String getEname() {
		return ename;
	}
	public int getSalary() {
		return salary;
	}
	public int getAge() {
		return age;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
